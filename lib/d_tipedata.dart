// Isi setiap fungsi dengan tipe data yang tepat!

void main() {
  print(getNama());
  print(getUmur());
  print(getTinggi());
  print(getIsWRIMember());
  print(getHobi());
  print(getAlamat());
  print(getAnggota());
}

// returnkan data kata/teks
dynamic getNama() {
  return "Virza";
}

// returnkan data bilangan bulat
dynamic getUmur() {
  return 12;
}

// returnkan data bilangan desimal
dynamic getTinggi() {
  return 1.7;
}

// returnkan data boolean
dynamic getIsWRIMember() {
  return true;
}

// returnkan data list
dynamic getHobi() {
  return ["virza", 1];
}

// returnkan data map
dynamic getAlamat() {
  return {"virza": "1"};
}

// returnkan data set
dynamic getAnggota() {
  return {2, 3};
}
