// Isi setiap fungsi dengan tipe data yang tepat!

void main() {
  print(getNama());
  print(getUmur());
  print(getTinggi());
  print(getIsWRIMember());
  print(getHobi());
  print(getAlamat());
  print(getAnggota());
}

// returnkan data kata/teks
dynamic getNama() {
  return "shofa" ;
}

// returnkan data bilangan bulat
dynamic getUmur() {
  return 19;
}

// returnkan data bilangan desimal
dynamic getTinggi() {
  return 1.3;
}

// returnkan data boolean
dynamic getIsWRIMember() {
  return true;
}

// returnkan data list
dynamic getHobi() {
  return ["ofa", 12, "whyyy"];
}

// returnkan data map
dynamic getAlamat() {
 return {"ofa": "hoem"};
}

// returnkan data set
dynamic getAnggota() {
  return {1,2};
}
