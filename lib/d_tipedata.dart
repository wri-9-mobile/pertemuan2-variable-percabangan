// Isi setiap fungsi dengan tipe data yang tepat!

void main() {
  print(getNama());
  print(getUmur());
  print(getTinggi());
  print(getIsWRIMember());
  print(getHobi());
  print(getAlamat());
  print(getAnggota());
}

// returnkan data kata/teks
dynamic getNama() {
  return 'Edo';
}

// returnkan data bilangan bulat
dynamic getUmur() {
  return 18;
}

// returnkan data bilangan desimal
dynamic getTinggi() {
  return 176.0;
}

// returnkan data boolean
dynamic getIsWRIMember() {
  return true;
}

// returnkan data list
dynamic getHobi() {
  return ['Main Game'];
}

// returnkan data map
dynamic getAlamat() {
  return {"alamat": "Bojonegoro"};
}

// returnkan data set
dynamic getAnggota() {
  return {1, 2, 3};
}
