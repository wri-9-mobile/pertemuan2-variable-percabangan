// Isi setiap fungsi dengan tipe data yang tepat!

void main() {
  print(getNama());
  print(getUmur());
  print(getTinggi());
  print(getIsWRIMember());
  print(getHobi());
  print(getAlamat());
  print(getAnggota());
}

// returnkan data kata/teks
dynamic getNama() {

  return "Fiqlal";
}

// returnkan data bilangan bulat
dynamic getUmur() {

  return 18;
}

// returnkan data bilangan desimal
dynamic getTinggi() {

return 0.1;

}

// returnkan data boolean
dynamic getIsWRIMember() {

return true;
}

// returnkan data list
dynamic getHobi() {

return ["GAME, MEMBACA"];
}

// returnkan data map
dynamic getAlamat() {

  return {
    'jalan' : 'Kebang turi',
    'kecamatan' : 'Lowokwaru',
    'kota' : 'Malang'

};

}

// returnkan data set
dynamic getAnggota() {

return {
  1,2,3
};
}
