// Isi setiap fungsi dengan tipe data yang tepat!

void main() {
  print(getNama());
  print(getUmur());
  print(getTinggi());
  print(getIsWRIMember());
  print(getHobi());
  print(getAlamat());
  print(getAnggota());
}

// returnkan data kata/teks
dynamic getNama() {
  return "Bril";
}

// returnkan data bilangan bulat
dynamic getUmur() {
  return 18;
}

// returnkan data bilangan desimal
dynamic getTinggi() {
  return 1.75;
}

// returnkan data boolean
dynamic getIsWRIMember() {
  return true;
}

// returnkan data list
dynamic getHobi() {
  return [];
}

// returnkan data map
dynamic getAlamat() {
  return {"Hobi": "Main game"};
}

// returnkan data set
dynamic getAnggota() {
  return {"bakso", "Mi"};
}
